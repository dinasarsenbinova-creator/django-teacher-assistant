def get_subject_key(subject):
    """Определяет ключ предмета для шаблонов"""
    if not subject:
        return 'default'
    
    subject_lower = subject.lower()
    
    if any(word in subject_lower for word in ['матем', 'алгебр', 'геометр', 'тригоном', 'арифмет']):
        return 'математика'
    elif any(word in subject_lower for word in ['физик', 'механ', 'оптик', 'электр']):
        return 'физика'
    else:
        return 'default'
